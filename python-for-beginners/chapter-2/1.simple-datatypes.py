# python has 4 simple data types

# integers - whole numbers, like 5
number = 5
# floats - decimal numbers, like 2.3
decimal = 2.3
# strings - like "this" (things in quotes)
string = "this"
# booleans - true/false statements
# the first letter in True or False in python is always a capital
boolean = True

# each one of these can be assigned to a variable, simply by stating variable_name = x

# there are other ways to assign number using Hexadecimal and binary, but we wont cover that beyond this example
# the following are also forms of integer
bin_var = 0x10100
hex_var = 0xc0ffee
