# when we define a class any attribute that starts with 'self.' is an instance attribute.

# we can also have attributes in a class that dont start with 'self.' and are written like normal variables
# these attributes are mutable however it is good practice to use getters and setters. 
# an instances attributes in python are always public. in practice to show that a variable is not public in python.
# we use a '__' prefix for a private attribute. and a '_' for protected attribute.

# 'class attributes' are attributes that are inside the class when initialised (in the blueprints and initial object)
# but arent in the object as a variable we can interact with.
