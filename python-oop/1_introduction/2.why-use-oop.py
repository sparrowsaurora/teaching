# Modularity:
# Code is organized into reusable "blueprints" (classes).

# Reusability:
# Once a class is written, it can be used to create multiple objects.

# Encapsulation:
# Hides internal complexity; keeps data safe from outside interference.
# Access is controlled using methods (getters/setters).

# Inheritance:
# Share code across classes (child class inherits from a parent).
# Promotes code reuse and establishes a natural hierarchy.

# Polymorphism:
# Different objects can respond to the same method in their own way.

# Scalability:
# Easier to grow and maintain large codebases.
# OOP structure supports adding new features with minimal changes.